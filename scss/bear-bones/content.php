<h2>Code</h2>
<h3>code</h3>
<p><code>&lt;code&gt;</code> is used for indicating code that is inline.</p>
<p>An example is if you were to show how to use the <code>&lt;element&gt;</code> tag correctly.</p>
<h3>kbc</h3>
<p><code>&lt;kbd&gt;</code> is used to indicate a keyboard stroke.</p>
<p>An example is if you had to press <kbd>Ctrl</kbd> + <kbd>C</kbd> to copy.</p>
<h3>pre</h3>
<p><code>&lt;pre&gt;</code> is used to display a block of multiple lines of code.</p>
<pre>
	<p>This is a paragraph</p>
	<div>This is a div</div>
</pre>
